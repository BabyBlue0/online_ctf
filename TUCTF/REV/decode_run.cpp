#include<iostream>
using namespace std;

const int passlen = 0x2c;
unsigned char password[passlen+1] = {
  0xFD, 0xFF, 0xD3, 0xFD, 0xD9, 0xA3, 0x93, 0x35,
  0x89, 0x39, 0xB1, 0x3D, 0x3B, 0xBF, 0x8D, 0x3D, 
  0x3B, 0x37, 0x35, 0x89, 0x3F, 0xEB, 0x35, 0x89,
  0xEB, 0x91, 0xB1, 0x33, 0x3D, 0x83, 0x37, 0x89,
  0x39, 0xEB, 0x3B, 0x85, 0x37, 0x3F, 0xEB, 0x99,
  0x8D, 0x3D, 0x39, 0xAF, 0x00 
};

int main()
{
  for( int i = 0; i < passlen; i++ ){
    unsigned char c = password[i];
    c ^= 0xAA;
    c = ~c;
    c >>= 1;
    c &= 0x7f;  // ASCII指定
    cout << c;
  }
  cout << endl;
  return 0;
}

